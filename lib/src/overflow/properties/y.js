const { styleClass } = require('../../../utils');

module.exports = (root, opts) => {
    const overflowY = {
        'overflow-y-auto': 'auto',
        'overflow-y-hidden': 'hidden',
        'overflow-y-visible': 'visible',
        'overflow-y-scroll': 'scroll'
    };

    styleClass('overflow-y', overflowY, root, opts, true);
};
